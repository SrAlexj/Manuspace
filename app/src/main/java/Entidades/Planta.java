package Entidades;

public class Planta {
    private String nombreplanta;
    private double areaplanta;

    public Planta() {
    }

    public String getNombreplanta() {
        return nombreplanta;
    }

    public void setNombreplanta(String nombreplanta) {
        this.nombreplanta = nombreplanta;
    }

    public double getAreaplanta() {
        return areaplanta;
    }

    public void setAreaplanta(double areaplanta) {
        this.areaplanta = areaplanta;
    }
}
