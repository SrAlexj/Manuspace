package Entidades;

public class Area {
    private String nombrearea;
    private double area;

    public Area() {
    }


    public String getNombrearea() {
        return nombrearea;
    }

    public void setNombrearea(String nombre) {
        this.nombrearea = nombre;
    }

    public double getArea() {
        return area;
    }

    public void setArea(double area) {
        this.area = area;
    }
}
